    public function changeIcons()
    {
        $res = [];
        foreach ($this->categories as $key => &$item) {
            $emojiOnly = preg_replace('/[^\p{So}\p{Sk}]/u', '', $item);
            $res[$key] = $emojiOnly;
        }

        $childs = self::all()->toArray();
        foreach ($childs as $child) {
            if(isset($res[$child['url']])) {
                self::where('id', $child['id'])->update(['parent_icon' => $res[$child['url']]]);
            }
        }

    }


